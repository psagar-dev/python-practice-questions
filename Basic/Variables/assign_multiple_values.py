#Assign Multiple Values
print("============== 1 ===================")
x, y,z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

#One Value to Multiple Variables
print("============== 2 ===================")
oneValueX = oneValueY = oneValueZ = "Orange"
print(oneValueX)
print(oneValueY)
print(oneValueZ)

#Unpack a Collection
print("============== 3 ===================")
fruits = ["apple", "banana", "cherry"]
unpackX, unpackY, unpackZ = fruits
print(unpackX)
print(unpackY)
print(unpackZ)