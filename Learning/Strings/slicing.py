#Slicing
print("============== 1 ===================")
b = "Hello, World!"
print(b,"=>", b[2:5])

#Slice From the Start
print("============== 2 ===================")
print(b,"=>", b[:5])

#Slice To the End
print("============== 3 ===================")
print(b,"=>",b[2:])

#Negative Indexing
print("============== 4 ===================")
print(b,"=>",b[-5:-2])