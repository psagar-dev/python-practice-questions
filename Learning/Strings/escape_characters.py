#Use the escape character \"
print("============== 1 ===================")
txtDoubleSlash = "We are the so-called \"Vikings\" from the north"
print(txtDoubleSlash)

# use the escape character \'
print("============== 2 ===================")
txtSingleSlash = 'it\'s alright'
print(txtSingleSlash)

#Back Slash
print("============== 3 ===================")
txtBackslash = "This will insert one \\ (backslash)."
print(txtBackslash)

#New Line
print("============== 4 ===================")
txtNewLine = "Hello\nworld!"
print(txtNewLine)

#Carriage Return
print("============== 5 ===================")
txtCarriageReturn = "Hello\rWorld!"
print(txtCarriageReturn)

#Tab
print("============== 6 ===================")
txtTab = "Hello\t\tWorld!"
print(txtTab)

#Backspace
print("============== 7 ===================")
txtBackspace = "Hello \bWorld"
print(txtBackspace)

#Octal value
print("============== 8 ===================")
txtOctalValue = "\110\145\154\154\157"
print(txtOctalValue)

#Hex value
print("============== 9 ===================")
txtHexValue = "\x48\x65\x6c\x6c\x6f"
print(txtHexValue)